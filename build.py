def native_name(lang):
    if lang == "zh-cn":
        return "中文 (简体)"
    if lang == "zh-tw":
        return "中文 (繁體)"
    locale = Locale.parse(lang.replace("-", "_"))
    return locale.get_display_name(locale)


def sorted_english_first(l):
    return sorted(l, key=lambda x: (x != "en", x))


def bcp47(lang):
    if "-" in lang:
        language, region = lang.split("-")
        return f"{language}-{region.upper()}"
    return lang


