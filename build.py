def extract_code(content):
    code_blocks = []
    in_code_block = False
    for line in content.split("\n"):
        # TODO: proper markdown parsing
        # TODO: don't extract from code blocks without language label?
        if line.startswith("```"):
            in_code_block = not in_code_block
            continue
        if in_code_block:
            code_blocks.append(line)
    return "\n".join(code_blocks) + "\n"


