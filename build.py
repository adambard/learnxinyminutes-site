def path(lang, language):
    return f"/{lang}/{language}/" if lang != "en" else f"/{language}/"


# Inherit metadata from English articles
for language in articles:
    for lang, article in articles[language].items():
        metadata = article.metadata
        if "en" in articles[language]:
            articles[language][lang].metadata = {
                **articles[language]["en"].metadata,
                **metadata,
            }

# Build HTML pages from articles
for language in articles:
    for lang, article in articles[language].items():
        metadata = article.metadata
        if "en" in articles[language]:
            metadata = {**articles[language]["en"].metadata, **metadata}

        content = article.content
        orig_path = metadata["orig_path"]

        name = metadata.get("name", language)
        filename = metadata.get("filename")
        context = {
            "name": name,
            "where_x_eq_name": metadata.get("where_x_eq_name", name),
            "filename": filename,
            "content": markdown_to_html(content),
            "lang": lang,
            "langs": sorted(articles[language].keys()),
            "contributors": metadata.get("contributors", []),
            "contributor_count": metadata.get("contributor_count", 0),
            "new_issue_url": f"{docs_repo}/issues/new",
            "edit_url": f"{docs_repo}/edit/master/{orig_path}",
            "blame_url": f"{docs_repo}/blame/master/{orig_path}",
            "i18n": i18n_data.get(lang.replace("-", "_"), i18n_data["default"]),
            "pluralize": pluralize,
            "twitter_share_url": twitter_share_url,
            "canonical": f"{lang + '/' if lang != 'en' else ''}{language}/",
            "current_year": date.today().year,
        }
        html = render_template("doc.html", context)

        output_path = output_dir if lang == "en" else output_dir / lang
        (output_path / language).mkdir(parents=True, exist_ok=True)
        with open(output_path / language / "index.html", "w") as f:
            f.write(html)

        if filename:
            (output_path / "files").mkdir(parents=True, exist_ok=True)
            code = extract_code(content)
            with open(output_path / "files" / filename, "w") as f:
                f.write(code)


categories = {
    "language": [],
    "framework": [],
    "tool": [],
    "Algorithms & Data Structures": [],
}
for language in articles:
    lang = sorted_english_first(articles[language].keys())[0]
    category = articles[language][lang].metadata.get("category", "language")
    categories[category].append(language)
for c in categories:
    categories[c] = sorted(
        categories[c],
        key=lambda x: articles[x][list(articles[x])[0]].metadata["name"].lower(),
    )

all_langs = set(l for article in articles.values() for l in article.keys())
all_langs = sorted_english_first(all_langs)


def render_language_index(lang):
    return render_template(
        "index.html",
        context={
            "categories": categories,
            "articles": articles,
            "lang": lang,
            "langs": [l for l in all_langs if l != lang],
            "i18n": i18n_data["default"],
            "get_category_display_name": get_category_display_name,
            "path": path,
            "canonical": f"{lang}/" if lang != "en" else "",
            "native_name": native_name,
        },
    )


for lang in all_langs:
    index_html = render_language_index(lang)
    outfile = output_dir / lang / "index.html"
    if lang == "en":
        outfile = output_dir / "index.html"
    with open(outfile, "w") as f:
        f.write(index_html)


def redirect(orig, to):
    redirect_html = render_template(
        "redirect.html",
        context={"to": to},
    )
    orig = output_dir / orig
    (orig).mkdir(parents=True, exist_ok=True)
    with open(orig / "index.html", "w") as f:
        f.write(redirect_html)


