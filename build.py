def highlight_code(html):
    formatter = pygments.formatters.HtmlFormatter(nowrap=True)

    code_expr = re.compile(
        r'<pre( lang="(?P<lang>[^"]+)")?><code>(?P<code>.+?)</code></pre>', re.DOTALL
    )

    def replacer(match) -> str:
        lang = match.group("lang")
        try:
            lexer = pygments.lexers.get_lexer_by_name(lang)
        except pygments.util.ClassNotFound:
            if lang is not None:
                print(f"Pygments doesn't support language: {lang}", file=sys.stderr)
            lang = None
            lexer = pygments.lexers.TextLexer()

        code = match.group("code")

        # Decode html entities in the code.
        code = unescape(code)

        highlighted = pygments.highlight(code, lexer, formatter)

        if lang:
            return (
                f'<div class="highlight"><pre lang="{lang}">{highlighted}</pre></div>'
            )
        return f'<div class="highlight"><pre>{highlighted}</pre></div>'

    result = code_expr.sub(replacer, html)

    return result


def markdown_to_html(raw):
    html = cmarkgfm.github_flavored_markdown_to_html(
        raw, options=cmarkgfm_options.CMARK_OPT_UNSAFE
    )
    return highlight_code(html)


def pluralize(value, count):
    if count == 1:
        return value + "-one"
    return value + "-other"


def count_contributors(path):
    try:
        result = subprocess.run(
            ["git", "blame", "--line-porcelain", "--", path.relative_to(docs_dir)],
            cwd=docs_dir,
            text=True,
            capture_output=True,
            check=True,
        )
        authors = [
            line.split(" ", 1)[1].strip()
            for line in result.stdout.splitlines()
            if line.startswith("author ")
        ]
        unique_authors = set(authors)
        return len(unique_authors)
    except subprocess.CalledProcessError as e:
        print(f"git blame error: {e.stderr}", end="")
        return 0


def twitter_share_url(url, doc):
    full_url = "https://learnxinyminutes.com" + (("/" + url) if url else "")
    text = doc or ""
    qs = urlencode({"url": full_url, "text": text})
    return f"https://twitter.com/intent/tweet?{qs}"


# Load all articles
articles = {}
for root, dirs, files in os.walk(docs_dir):
    dirs[:] = [d for d in dirs if not d.startswith(".") and d not in IGNORE]

    for file in files:
        if file.startswith(".") or file in IGNORE:
            continue
        filename = Path(root) / file

        if filename.name.endswith(".md") or filename.name.endswith(".markdown"):
            language = filename.stem.split(".")[0]

            lang = "en"
            if len(filename.parts) > 3:
                lang = filename.parts[2]

            article = frontmatter.load(filename)

            name_key = {
                "Algorithms & Data Structures": "name",
                "framework": "framework",
                "language": "language",
                "tool": "tool",
            }[article.metadata.get("category", "language")]
            if name_key in article.metadata:
                article.metadata["name"] = article.metadata.get(
                    name_key, article.metadata.get("name")
                )
            article.metadata["orig_path"] = filename.relative_to(docs_dir)
            article.metadata["contributor_count"] = count_contributors(filename)

            if language not in articles:
                articles[language] = {}
            if lang in articles[language]:
                print(f"Duplicate article for {language} in {lang}")
            articles[language][lang] = article
        else:
            # copy the file
            (output_dir / filename.relative_to(docs_dir)).parent.mkdir(
                parents=True, exist_ok=True
            )
            shutil.copy(filename, output_dir / filename.relative_to(docs_dir))

